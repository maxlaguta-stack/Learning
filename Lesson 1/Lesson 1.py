age = int(input("Сколько тебе лет? "))
discount = input("Есть ли у тебя скидочная карта клуба? ").lower()
time = int(input("Время посещения? "))

error = False

if age <= 0 or age > 120:
    print("Некорректный возраст")
    error = True

if time < 0 or time > 23:
    print("Некорректное время")
    error = True

if error:
    # Если хоть одна ошибка — не проверяем доступ
    pass
else:
    # Основная логика проверки доступа
    if age < 16:
        print("Вход запрещён: слишком молод")
    elif 16 <= age <= 17:
        if 8 <= time <= 20:
            print("Доступ разрешён (детский тариф)")
        else:
            print("Вход запрещён: после 20 только для взрослых")
    elif 18 <= age <= 60:
        if discount == "да":
            print("Доступ разрешён (взрослый, со скидкой 10%)")
        else:
            print("Доступ разрешён (взрослый)")
    elif age > 60:
        if 8 <= time <= 18:
            print("Доступ разрешён (льготный тариф)")
        else:
            print("Вход запрещён: вечером только до 60 лет")
    else:
        print("Что-то пошло не так... проверь ввод")
